public class QuotaBean
{
    private int quota;
    private String result;
    private int max;
    
    public int getQuota()
    {
        return quota;
    }
    
    public void setQuota(int quota)
    {
        this.quota = quota;
    }
    
    public String getResult()
    {
        return result;
    }
    
    public void setResult(String result)
    {
        this.result = result;
    }
    
    public int getMax()
    {
        return max;
    }
    
    public void setMax(int max)
    {
        this.max = max;
    }
}
